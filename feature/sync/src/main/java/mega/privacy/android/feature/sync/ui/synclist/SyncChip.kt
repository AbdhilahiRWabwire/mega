package mega.privacy.android.feature.sync.ui.synclist

/**
 * Enumeration of the Sync chips
 */
enum class SyncChip {
    /**
     * Chip to display the synced folders list
     */
    SYNC_FOLDERS,

    /**
     * Chip to display the stalled issues list
     */
    STALLED_ISSUES,

    /**
     * Chip to display the solved issues list
     */
    SOLVED_ISSUES
}
