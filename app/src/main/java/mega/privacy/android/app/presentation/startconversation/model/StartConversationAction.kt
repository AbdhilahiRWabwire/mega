package mega.privacy.android.app.presentation.startconversation.model

/**
 * Enum class defining the available actions for starting a new chat conversation.
 */
enum class StartConversationAction {

    /**
     * New group chat.
     */
    NewGroup,

    /**
     * New meeting.
     */
    NewMeeting,

    /**
     * Join an existing meeting.
     */
    JoinMeeting
}