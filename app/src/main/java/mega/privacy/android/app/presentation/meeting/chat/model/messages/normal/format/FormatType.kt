package mega.privacy.android.app.presentation.meeting.chat.model.messages.normal.format

/**
 * Enum class defining all the possible format types for a text.
 */
enum class FormatType {
    /**
     * Bold.
     */
    Bold,

    /**
     * Italic.
     */
    Italic,

    /**
     * Strikethrough.
     */
    Strikethrough,

    /**
     * Quote.
     */
    Quote,

    /**
     * Multi-quote.
     */
    MultiQuote,

    /**
     * None.
     */
    None
}