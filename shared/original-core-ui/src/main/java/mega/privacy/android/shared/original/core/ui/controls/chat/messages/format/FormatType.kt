package mega.privacy.android.shared.original.core.ui.controls.chat.messages.format

/**
 * Enum class defining all the possible format types for a text.
 */
enum class FormatType {
    /**
     * Bold.
     */
    Bold,

    /**
     * Italic.
     */
    Italic,

    /**
     * Strikethrough.
     */
    Strikethrough,

    /**
     * Quote.
     */
    Quote,

    /**
     * Multi-quote.
     */
    MultiQuote,

    /**
     * Link.
     */
    Link
}