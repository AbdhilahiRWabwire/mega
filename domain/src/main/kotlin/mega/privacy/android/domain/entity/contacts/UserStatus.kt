package mega.privacy.android.domain.entity.contacts

/**
 * Enum class defining the possible user statuses.
 */
enum class UserStatus {

    /**
     * Offline status.
     */
    Offline,

    /**
     * Away status.
     */
    Away,

    /**
     * Online status.
     */
    Online,

    /**
     * Busy status.
     */
    Busy,

    /**
     * Invalid status.
     */
    Invalid
}