package mega.privacy.android.domain.entity.search

/**
 * Enum class to represent the types filter options
 */
enum class TypeFilterOption {
    /**
     * Images filter option
     */
    Images,

    /**
     * Documents filter option
     */
    Documents,

    /**
     * Audio filter option
     */
    Audio,

    /**
     * Videos filter option
     */
    Video,

    /**
     * Pdf filter option
     */
    Pdf,

    /**
     * Presentation filter option
     */
    Presentation,

    /**
     * Spreadsheet filter option
     */
    Spreadsheet,

    /**
     * Folder filter option
     */
    Folder,

    /**
     * Other filter option
     */
    Other
}
